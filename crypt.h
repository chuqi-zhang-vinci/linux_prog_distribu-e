#ifndef _CRYPT_H_
#define _CRYPT_H_

/**
 * PRE : tableau de char msg à crypter
 * POST : le message sera crypté
 * RES : affiche le message crypté
 */

/* déclaration de fonctions */
char* encrypt (char* msg);


/**
 * PRE : tableau de char msg à décrypter
 * POST : le message sera décrypté
 * RES : affiche le message décrypté
 */

/* déclaration de fonctions */
char* decrypt (char* msg);



#endif